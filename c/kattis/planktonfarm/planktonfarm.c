#include <stdio.h>
#include <stdlib.h>

void merge(long arr[], long l, long m, int r)
{
    int i, j, k;
    int n1 = m - l + 1;
    int n2 = r - m;
 
    /* create temp arrays */
    int L[n1], R[n2];
 
    /* Copy data to temp arrays L[] and R[] */
    for (i = 0; i < n1; i++)
        L[i] = arr[l + i];
    for (j = 0; j < n2; j++)
        R[j] = arr[m + 1 + j];
 
    /* Merge the temp arrays back into arr[l..r]*/
    i = 0; // Initial index of first subarray
    j = 0; // Initial index of second subarray
    k = l; // Initial index of merged subarray
    while (i < n1 && j < n2) {
        if (L[i] <= R[j]) {
            arr[k] = L[i];
            i++;
        }
        else {
            arr[k] = R[j];
            j++;
        }
        k++;
    }
 
    /* Copy the remaining elements of L[], if there
    are any */
    while (i < n1) {
        arr[k] = L[i];
        i++;
        k++;
    }
 
    /* Copy the remaining elements of R[], if there
    are any */
    while (j < n2) {
        arr[k] = R[j];
        j++;
        k++;
    }
}
 
/* l is for left index and r is right index of the
sub-array of arr to be sorted */
void mergeSort(long arr[], long l, long r)
{
    if (l < r) {
        // Same as (l+r)/2, but avoids overflow for
        // large l and h
        int m = l + (r - l) / 2;
 
        // Sort first and second halves
        mergeSort(arr, l, m);
        mergeSort(arr, m + 1, r);
 
        merge(arr, l, m, r);
    }
}

int main() {
    long a, b, c[100000];
    scanf("%lu %lu", &a, &b);
    for (int i = 0; i < b; i++) {
        long curr;
        scanf("%lu", &curr);
        c[i] = curr;
    }
    
    mergeSort(c, 0, b - 1);

    int ptr1 = 0, ptr2 = b - 1, ans = 0;
    while (ptr1 < ptr2) {
        if (c[ptr1] * c[ptr2] < a) {
            ptr1++;
        } else {
            ptr1++;
            ptr2--;
            ans++;
        }
    }
    printf("%lu\n", ans);
}
